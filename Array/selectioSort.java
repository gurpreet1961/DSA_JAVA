/**
 * 5, 2, -1, 6, 3...............................................................
 * find smallest element (-1)...................................................
 * swap 5 and -1................................................................
 * -1, 2, 5, 6, 3...............................................................
 * find smallest element (2)....................................................
 * swap here 2 is at it's right position........................................
 * find smallest element (3)....................................................
 * swap 5 and 3.................................................................
 * -1, 2, 3, 6, 5...............................................................
 * find smallest element (5)....................................................
 * swap 5 and 6................................................................
 * -1, 2, 3, 5, 6...............................................................
 * 
 * 
 */

public class selectioSort {
    public static void main(String[] args) {
        int[] a = { 2, 1, 4, 2, 4 };
        for (int i = 0; i < a.length - 1; i++) {
            int minIndex = i;
            for (int j = i; j < a.length; j++) {
                if (a[j] < a[minIndex]) {
                    minIndex = j;
                }
            }
            int temp = a[i];
            a[i] = a[minIndex];
            a[minIndex] = temp;

        }
        for (int ele : a) {
            System.out.println(ele);
        }
    }
}
